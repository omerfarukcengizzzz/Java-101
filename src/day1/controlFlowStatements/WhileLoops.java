package day1.controlFlowStatements;

public class WhileLoops {
    public static void main(String[] args) {

        int i = 1;

        while (i <= 10) {
            System.out.println(i);
            i++;
        }

        System.out.println("========================");

        int a = 1;

        do {
            System.out.println(a);
        } while (a > 5);

    }
}
